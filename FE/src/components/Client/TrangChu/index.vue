<script>
export default {
  name: "HomePage",

     mounted() {
    this.initCarousel();
  },

  watch: {
    $route() {
      this.$nextTick(() => {
        this.initCarousel();
      });
    },
  },

  methods: {
  beforeUnmount() {
  const $carousel = $(".header-carousel");

  if ($carousel.length && $carousel.hasClass("owl-loaded")) {
    $carousel.trigger("destroy.owl.carousel");
  }
},
initCarousel() {
  this.$nextTick(() => {
    const $carousel = $(".header-carousel");

    if (window.$ && $carousel.length) {
      // destroy carousel cũ
      if ($carousel.hasClass("owl-loaded")) {
        $carousel.trigger("destroy.owl.carousel");

        $carousel.removeClass("owl-loaded owl-hidden");
        $carousel.find(".owl-stage-outer").children().unwrap();
        $carousel.find(".owl-stage").children().unwrap();
        $carousel.find(".owl-item").children().unwrap();

        $carousel.find(".owl-stage-outer").remove();
        $carousel.find(".owl-nav").remove();
        $carousel.find(".owl-dots").remove();
      }

      // khởi tạo lại
      $carousel.owlCarousel({
        autoplay: true,
        autoplayTimeout: 5000,
        smartSpeed: 1200,
        items: 1,
        loop: true,
        dots: true,
        nav: true,
        margin: 0,
        navText: [
          '<i class="bi bi-chevron-left"></i>',
          '<i class="bi bi-chevron-right"></i>',
        ],
      });
    }
  });
},
  },
};
</script>
